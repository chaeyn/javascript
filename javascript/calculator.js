const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  divide: function (a, b) {
    console.log(a / b);
  },
  square: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(5, 8);
calculator.minus(5, 8);
calculator.divide(5, 8);
calculator.square(5, 8);
